public class operaciones_matematicas_basicas9 
{
    public static void main(String[] args) 
    {
        /**MIRAR CAPTURA OPERADORES LOGICOS BASICOS DIAPOSITIVA 74
         * + suma o signo         
         * * - resta o signo         
         * * * multiplicacion         
         * * / division
         * % modulo         
         * * ++ incremento en 1         
         * * -- decremento en 1         
         * * += suma y asignacion
         * -= resta y asignacion         
         * *= multiplicacion y asignacion         
         * /= division y asignacion 
         * %= modulo y asignacion
         * */

        // Para aumentar el valor de a en 1 podriamos hacerlo de estas 3 maneras:
        int a = 1;  //a vale 1
        a = a+1;    // a vale 2
        System.out.println(a);
        a += 1; // a vale 3
        System.out.println(a);
        a++;    // a vale 4
        System.out.println(a);

        // Los operadores de incremento y decremento se pueden escribir antes o despues de la variable:
        a++;
        ++a;

       // Hay una diferencia si el valor se asigna a otra variable al mismo tiempo. VER DIAPOSITIVA 78.
    


    }
}
