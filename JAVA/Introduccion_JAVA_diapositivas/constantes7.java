public class constantes7 
{
    public static void main(String[] args) 
    {
        // Las CONSTANTES son variables con con valor fijo, soporta nombres, se recomienda
        // que se declaren en MAYUSCULAS y se crean de la siguiente forma:
        final float PI = 3.14f;
        final String NOMBRE_CANAL = "Aulaenlanube";
        // La palabra clave es FINAL para que los valores no puedan ser modificados.

        System.out.println(PI);
        System.out.println(NOMBRE_CANAL);
    }  
}